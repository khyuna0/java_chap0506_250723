package ch06_03_Exer;

public class PrinterEx {

	// 메소드 오버로딩
	public void println(int v) {
		System.out.println(v);
	}
	
	public void println(boolean v) {
		System.out.println(v);
	}
	
	public void println(double v) {
		System.out.println(v);
	}
	
	public void println(String v) {
		System.out.println(v);
	}
}
